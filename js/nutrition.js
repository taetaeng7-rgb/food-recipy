function factor(unit, value, per) {
  if (value == null) return null;
  if (per === '100g') {
    if (unit === 'g' || unit === 'ml') return value / 100;
    if (unit === '큰술') return (value * 12) / 100; // 대략 1큰술=12g
    if (unit === '작은술') return (value * 4) / 100;
    if (unit === '컵') return (value * 200) / 100;
    return null;
  }
  if (per === 'ea') {
    if (['개', '모', '대', '장', '쪽', '줌'].includes(unit)) return value;
    return null;
  }
  if (per === 'tbsp') {
    if (unit === '큰술') return value;
    if (unit === '작은술') return value / 3;
    if (unit === 'ml') return value / 15;
    if (unit === '컵') return (value * 200) / 15;
    return null;
  }
  if (per === 'tsp') {
    if (unit === '작은술') return value;
    if (unit === '큰술') return value * 3;
    return null;
  }
  return null;
}

function lookup(nameKo) {
  for (const [re, per, kcal, c, p, f] of TABLE) if (re.test(nameKo)) return { per, kcal, c, p, f };
  return null;
}

// scaledRecipe: scaleRecipe() 결과 (ingredients[].value/.unit, targetServings)
// 반환: { perServing:{kcal,carb,protein,fat}, matched, total, coverage } 또는 null
export function estimateNutrition(scaledRecipe) {
  let kcal = 0, carb = 0, protein = 0, fat = 0, matched = 0, total = 0;
  for (const ing of scaledRecipe.ingredients) {
    if (ing.scaleType === 'to-taste' || ing.value == null) continue;
    total += 1;
    const e = lookup(ing.name.ko);
    if (!e) continue;
    const fac = factor(ing.unit, ing.value, e.per);
    if (fac == null) continue;
    matched += 1;
    kcal += fac * e.kcal; carb += fac * e.c; protein += fac * e.p; fat += fac * e.f;
  }
  if (total === 0) return null;
  const n = scaledRecipe.targetServings || 1;
  return {
    perServing: {
      kcal: Math.round(kcal / n),
      carb: Math.round(carb / n),
      protein: Math.round(protein / n),
      fat: Math.round(fat / n),
    },
    matched, total, coverage: matched / total,
  };
}
