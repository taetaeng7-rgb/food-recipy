// 레시피의 재료명(ko)에 매칭되는 대체 항목(ctx:true) 반환 — 가이드 순서 유지, 중복 제거
export function subsFor(recipe) {
  const names = (recipe.ingredients || []).map((i) => i.name && i.name.ko).filter(Boolean);
  const out = [];
  for (const entry of SUBSTITUTES) {
    if (!entry.ctx) continue;
    if (names.some((n) => entry.match.test(n)) && !out.includes(entry)) out.push(entry);
  }
  return out;
}
